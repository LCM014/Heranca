public class Arvore 
{
    private String raizes;
    private String folhas;
    private int tronco;
    private double preco;

    public String getRaizes() 
    {
        return raizes;
    }
    public void setRaizes(String raizes) 
    {
        this.raizes = raizes;
    }
    public String getFolhas() 
    {
        return folhas;
    }
    public void setFolhas(String folhas) 
    {
        this.folhas = folhas;
    }
    public int getTronco() 
    {
        return tronco;
    }
    public void setTronco(int tronco) 
    {
        this.tronco = tronco;
    }
    public double getPreco() 
    {
        return preco;
    }
    public void setPreco(double preco) 
    {
        this.preco = preco;
    }
    
}
